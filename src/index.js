function loadData(data) {
    new gridjs.Grid({
        columns: [{
            id: 'character',
            name: 'Emoji'
        }, {
            id: 'unicodeName',
            name: 'Unicode Name'
        }, {
            id: 'group',
            name: 'Group'
        }, {
            id: 'subGroup',
            name: 'Sub Group'
        }],
        search: true,
        sort: true,
        data: data.concat(ASCII)
    }).render(document.getElementById("wrapper"));
}
